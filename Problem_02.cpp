int main ()
{
    int n , a;

    cin >> n;

    if (n <=100)
    {
        for (int i =0; i<n; i++)
        {
            cin >>a;

            if (a >= 0)
            {
                if (a%2 == 0)cout << "even" <<endl;
                else cout << "odd" <<endl;
            }
        }




    }






}
